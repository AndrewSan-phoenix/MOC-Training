<script>
    // Mobile menu toggle with animation
    const menuToggle = document.getElementById('menu-toggle');
    const mobileMenu = document.getElementById('mobile-menu');
    const menuIcon = document.getElementById('menu-icon');

    menuToggle.addEventListener('click', () => {
        mobileMenu.classList.toggle('hidden');

        // If menu is now visible
        if (!mobileMenu.classList.contains('hidden')) {
            // Change hamburger to X
            menuIcon.setAttribute('d', 'M6 18L18 6M6 6l12 12');
            // Animate menu appearance
            mobileMenu.classList.remove('-translate-y-2', 'opacity-0');
            mobileMenu.classList.add('translate-y-0', 'opacity-100');
        } else {
            // Change X back to hamburger
            menuIcon.setAttribute('d', 'M4 6h16M4 12h16M4 18h16');
            // Animate menu disappearance
            mobileMenu.classList.add('-translate-y-2', 'opacity-0');
            mobileMenu.classList.remove('translate-y-0', 'opacity-100');
        }
    });

    // Enhanced Swiper initialization
    var swiper = new Swiper(".mySwiper", {
        loop: true,
        effect: "fade",
        fadeEffect: {
            crossFade: true
        },
        autoplay: {
            delay: 4000,
            disableOnInteraction: false,
        },
        pagination: {
            el: ".swiper-pagination",
            clickable: true,
        },
        navigation: {
            nextEl: ".swiper-button-next",
            prevEl: ".swiper-button-prev",
        },
    });

    // Sticky header on scroll
    const header = document.querySelector('header');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            header.classList.add('shadow-md', 'py-2');
            header.classList.remove('py-3');
        } else {
            header.classList.remove('shadow-md', 'py-2');
            header.classList.add('py-3');
        }
    });

    // Back to top button
    const backToTopButton = document.getElementById('back-to-top');

    window.addEventListener('scroll', () => {
        if (window.scrollY > 300) {
            backToTopButton.classList.remove('opacity-0', 'invisible');
            backToTopButton.classList.add('opacity-100', 'visible');
        } else {
            backToTopButton.classList.add('opacity-0', 'invisible');
            backToTopButton.classList.remove('opacity-100', 'visible');
        }
    });

    backToTopButton.addEventListener('click', () => {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    });

    // Smooth scroll for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();

            const targetId = this.getAttribute('href');
            if (targetId === '#') return;

            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                targetElement.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });

                // Close mobile menu if open
                if (!mobileMenu.classList.contains('hidden')) {
                    menuToggle.click();
                }
            }
        });
    });

    // Animate elements on scroll
    const animateOnScroll = () => {
    const elements = document.querySelectorAll('.service-card, .text-center, .course-card');
    elements.forEach(element => {
        const elementPosition = element.getBoundingClientRect().top;
        const screenPosition = window.innerHeight / 1.2;
        if (elementPosition < screenPosition) {
            element.classList.add('animate-fadeIn');
            element.classList.remove('opacity-0'); // Ensure opacity is reset
        }
    });
};

window.addEventListener('load', () => {
    animateOnScroll(); // Run on page load
});
window.addEventListener('scroll', animateOnScroll);





